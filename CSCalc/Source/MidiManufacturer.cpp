juce::String MidiManufacturer::getManufacturerName(uint32_t id)
{
    for (int i = 0; manufacturers[i].name != nullptr; ++i)
    {
        if (manufacturers[i].id == id)
        {
            return juce::String(manufacturers[i].name);
        }
    }
    return "Unknown Manufacturer";
}
